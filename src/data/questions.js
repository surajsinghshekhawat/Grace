// Helper to get all questions as a flat array
export const getAllQuestions = () => {
  return questionSections.flatMap((section) => section.questions);
};

// One-time baseline questions (asked at setup, not every week)
export const getBaselineQuestions = () => {
  const baselineSectionIds = new Set(["identification", "demographics"]);
  return questionSections
    .filter((section) => baselineSectionIds.has(section.id))
    .flatMap((section) => section.questions);
};

// Repeat/weekly assessment questions (exclude identification + demographics)
export const getWeeklyQuestions = () => {
  const excludeSectionIds = new Set(["identification", "demographics"]);
  return questionSections
    .filter((section) => !excludeSectionIds.has(section.id))
    .flatMap((section) => section.questions);
};

// Helper to get total question count
export const getTotalQuestions = () => {
  return getAllQuestions().length;
};


