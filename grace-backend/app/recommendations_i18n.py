def normalize_lang(lang: str | None) -> str:
    if not lang:
        return "en"
    l = lang.strip().lower()
    if l.startswith("hi"):
        return "hi"
    if l.startswith("ta"):
        return "ta"
    return "en"


def localize_recommendation_row(row: dict, lang: str | None) -> dict:
    """Return copy of row with name/effect translated when available."""
    n = normalize_lang(lang)
    if n == "en":
        return dict(row)
    rid = row.get("rec_id") or ""
    pack = RECOMMENDATION_I18N.get(rid, {}).get(n)
    if not pack:
        return dict(row)
    out = dict(row)
    out["name"] = pack["name"]
    out["effect"] = pack["effect"]
    return out


def recommendations_for_api(recs: list[dict], lang: str | None) -> list[dict]:
    """Strip internal rec_id; apply i18n for hi/ta."""
    out: list[dict] = []
    for r in recs:
        loc = localize_recommendation_row(r, lang)
        out.append(
            {
                "name": loc["name"],
                "effect": loc["effect"],
                "resource_slug": loc.get("resource_slug", "learning_growth"),
            }
        )
    return out
